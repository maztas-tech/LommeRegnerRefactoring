package Files;

public class Calculator {

    public double add(double førsteVærdi, double andenVærdi) {
        return førsteVærdi + andenVærdi;
    }

    public double subtract(double førsteVærdi, double andenVærdi) {
        return førsteVærdi - andenVærdi;
    }

    public double multiply(double førsteVærdi, double andenVærdi) {
        return førsteVærdi * andenVærdi;
    }

    public double divide(double førsteVærdi, double andenVærdi) {
        return førsteVærdi / andenVærdi;
    }
}
